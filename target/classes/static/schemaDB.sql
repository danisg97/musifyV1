CREATE TABLE IF NOT EXISTS PEOPLE (
	ID LONG NOT NULL AUTO_INCREMENT,
	NAME VARCHAR(40) NOT NULL,
	YEARS INTEGER,
	MEMBER LONG,
	PRIMARY KEY (ID),
	CONSTRAINT FK_MEMBER FOREIGN KEY (MEMBER)
	REFERENCES ARTIST(ID)
);

