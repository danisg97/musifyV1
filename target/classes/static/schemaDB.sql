CREATE TABLE IF NOT EXISTS ARTIST (
    ID LONG NOT NULL AUTO_INCREMENT,
    NAME VARCHAR(40) NOT NULL,
    YEAR INTEGER,
    PRIMARY KEY (ID)
);

